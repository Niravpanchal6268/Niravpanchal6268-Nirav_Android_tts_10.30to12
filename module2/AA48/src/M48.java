import java.util.HashSet;

/*Write a Java program to append the specified element to the end of a 
hash set. */
public class M48 {
		public static void main(String[] args) {
			
			HashSet<String > hash = new HashSet<String>();
			hash.add("C");
			hash.add("C++");
			hash.add("Java");
			hash.add("Python");
			hash.add("JS");
			hash.add("HTML");
			
			
			System.out.println(hash);
			
		}
}
