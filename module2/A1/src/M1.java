/*W.A.J.P to get the character at the given index within the String. Original 
String = Tops Technologies! The character at position 0 is T, The character 
at position 10 is o*/


public class M1 {
	public static void main(String[] args) {
		String  t ="Tops Technologies!";
		
		System.out.println("String::"+t);
		
		
		
		System.out.println("The character at position 0 is::" + t.charAt(0));
		System.out.println("the Character at position 10 is::"+t.charAt(10));
		
	}

}
